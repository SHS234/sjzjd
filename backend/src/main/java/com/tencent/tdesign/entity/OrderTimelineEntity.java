package com.tencent.tdesign.entity;

import java.time.LocalDateTime;

public class OrderTimelineEntity {
    private Long id;
    private Long orderId;
    private Long operatorId; // User ID of who performed the action
    private String operatorName; // Cache name for display
    private String action; // e.g., "CREATE", "ACCEPT", "START", "UPLOAD_PROOF"
    private String content; // Display text, e.g., "打手A接单"
    private String metaData; // JSON for extra info, e.g. { "score_change": 200, "image_url": "..." }
    private LocalDateTime createdAt;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getOrderId() {
        return orderId;
    }

    public void setOrderId(Long orderId) {
        this.orderId = orderId;
    }

    public Long getOperatorId() {
        return operatorId;
    }

    public void setOperatorId(Long operatorId) {
        this.operatorId = operatorId;
    }

    public String getOperatorName() {
        return operatorName;
    }

    public void setOperatorName(String operatorName) {
        this.operatorName = operatorName;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getMetaData() {
        return metaData;
    }

    public void setMetaData(String metaData) {
        this.metaData = metaData;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }
}
